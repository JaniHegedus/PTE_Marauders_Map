package com.example.pte_marauders_map;

public class Variables {
    public static int activity;

    public static int getActivity() {
        return Variables.activity;
    }
    public static void setActivity(int activity) {
        Variables.activity = activity;
    }

    public static boolean started;
    public static boolean getStarted() {
        return Variables.started;
    }
    public static void setStartedOn(boolean b) {
        Variables.started = b;
    }
}
